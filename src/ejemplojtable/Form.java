        fila2[2] = p2.getPrecio_Producto();
        fila2[3] = p2.getCantidad_producto();
        
        modeloTabla.addRow(fila2);
        
        //Pasamos el modelo al Jtable con el metodo setModel para que lo muestre
        jTable1.setModel(modeloTabla);
        
