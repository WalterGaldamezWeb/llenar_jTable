        fila[0] = p.getId_producto();
        fila[1] = p.getNombre_producto();
        fila[2] = p.getPrecio_Producto();
        fila[3] = p.getCantidad_producto();
        
        //Agregamos la fila al modelo con el metodo addRow
        modeloTabla.addRow(fila);
        
        Object [] fila2 = new Object[4];
        fila2[0] = p2.getId_producto();
